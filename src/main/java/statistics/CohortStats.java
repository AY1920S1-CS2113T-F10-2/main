package statistics;

public class CohortStats {
    public String a;
    public Integer b;
    public Integer c;
    public Integer d;

    public CohortStats(String a, Integer b, Integer c, Integer d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public String getA() {
        return a;
    }

    public Integer getB() {
        return b;
    }

    public Integer getC() {
        return c;
    }

    public Integer getD() { return d; }
}
