package statistics;

public class GraduateStats {
    public String a;
    public Double b;
    public Integer c;

    public GraduateStats(String a, Double b, Integer c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public String getA() {
        return a;
    }

    public Double getB() {
        return b;
    }

    public Integer getC() {
        return c;
    }
}
